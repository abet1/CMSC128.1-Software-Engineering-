package com.example.project.model.enums;

/**
 * Payment Allocation Status Enum
 * Used for tracking payment status of individual allocations in group expenses
 * UNPAID: Not yet paid
 * PARTIALLY_PAID: Partially paid
 * PAID: Fully paid
 */
public enum PaymentAllocationStatus {
    UNPAID,
    PARTIALLY_PAID,
    PAID
}

